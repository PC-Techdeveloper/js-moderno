/*
OPERADORES DE COMPARACIÓN O RELACIONALES

- Mayor/Menor/Igual que -> >, >=, <, <=
- Igualdad/Igualdad estricta -> ==, ===
- Diferente/Diferente estricta -> !=, !==
*/

//COMPARACIÓN DE CADENAS -> Cuenta caracter por caracter
console.log('Z' > 'A')
console.log('Cat' >= 'Lion')

//COMPARACIÓN DE DIFERENTES TIPOS
console.log(1 > '1')
console.log(false == 0)
console.log(0.1 == 'chicken')
console.log(Boolean(0) == '0')

//COMPARACIÓN CON NULOS Y INDEFINIDOS
console.log(null === undefined)
console.log(null === null)
console.log(undefined === undefined)

